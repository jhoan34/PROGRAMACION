/*
En JavaScript, el objeto Math ofrece una amplia variedad de métodos matemáticos útiles para realizar cálculos. Aquí tienes una lista de los métodos más comunes de Math:

Métodos Básicos
Math.abs(x): Devuelve el valor absoluto de x.
Math.ceil(x): Redondea x hacia arriba al entero más cercano.
Math.floor(x): Redondea x hacia abajo al entero más cercano.
Math.round(x): Redondea x al entero más cercano.
Math.trunc(x): Devuelve la parte entera de x, eliminando los decimales.
Potencias y Raíces
Math.pow(base, exponente): Devuelve base elevado a exponente.
Math.sqrt(x): Devuelve la raíz cuadrada de x.
Math.cbrt(x): Devuelve la raíz cúbica de x.
Math.hypot(x, y, ...): Calcula la raíz cuadrada de la suma de los cuadrados de sus argumentos.
Logaritmos y Exponenciales
Math.log(x): Devuelve el logaritmo natural (base e) de x.
Math.log10(x): Devuelve el logaritmo en base 10 de x.
Math.log2(x): Devuelve el logaritmo en base 2 de x.
Math.exp(x): Devuelve e elevado a la potencia de x.
Trigonometría
Math.sin(x): Devuelve el seno de x (en radianes).
Math.cos(x): Devuelve el coseno de x (en radianes).
Math.tan(x): Devuelve la tangente de x (en radianes).
Math.asin(x): Devuelve el arco seno de x.
Math.acos(x): Devuelve el arco coseno de x.
Math.atan(x): Devuelve el arco tangente de x.
Math.atan2(y, x): Devuelve el arco tangente del cociente y / x.
Funciones Hiperbólicas
Math.sinh(x): Devuelve el seno hiperbólico de x.
Math.cosh(x): Devuelve el coseno hiperbólico de x.
Math.tanh(x): Devuelve la tangente hiperbólica de x.
Aleatoriedad
Math.random(): Genera un número pseudoaleatorio entre 0 (incluido) y 1 (excluido).
Máximo y Mínimo
Math.max(a, b, ...): Devuelve el valor máximo de una lista de números.
Math.min(a, b, ...): Devuelve el valor mínimo de una lista de números.
Constantes en Math
Math.PI: La constante π (~3.14159).
Math.E: La constante de Euler, base de los logaritmos naturales (~2.718).
Math.LN2: Logaritmo natural de 2.
Math.LN10: Logaritmo natural de 10.
Math.LOG2E: Logaritmo de E en base 2.
Math.LOG10E: Logaritmo de E en base 10.
Math.SQRT2: Raíz cuadrada de 2.
Math.SQRT1_2: Raíz cuadrada de 1/2.
Estos métodos y constantes te permiten realizar desde operaciones aritméticas básicas hasta cálculos más complejos en JavaScript.
*/